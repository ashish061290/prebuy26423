for (var i=1;i<6;i++) {
itemToClone = itemToClone.next();

if (!itemToClone.length) {
itemToClone = $(this).siblings(':fi<i class="fas fa-rupee-sign index-ruppee-small"></i>t');
}

itemToClone.children(':fi<i class="fas fa-rupee-sign index-ruppee-small"></i>t-child').clone()
.addClass("cloneditem-"+(i))
.appendTo($(this));
}
});
});

